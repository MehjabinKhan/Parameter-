// Parameter
function finalResult(totalMarks: number , obtainMarks: number): number {
    return obtainMarks / totalMarks * 100 ;
}
let marks: number = finalResult(550 , 400)
console.log(marks);

function sum(num: number , num2: number): number {
    return num + num2 ;
}
let totalSum: number = sum(111 , 789)
console.log(totalSum);

